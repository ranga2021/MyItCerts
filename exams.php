    <script>
        document.addEventListener('DOMContentLoaded', function() {
    // Selectors for the exams section
    const examCategoryList = document.querySelectorAll('.exams-listing-section .category-list li');
    const examCards = document.querySelectorAll('.exams-listing-section .exam-card');

    // Assign data-category to each exam card based on its title
    examCards.forEach(card => {
        const title = card.querySelector('.exam-content h3').textContent;
        let category = 'Other';
        if (/computer|software|architecture|data/i.test(title)) {
            category = 'Computer Science';
        } else if (/economics|accounting|C954|management/i.test(title)) {
            category = 'Business';
        } else if (/humanities/i.test(title)) {
            category = 'Humanities';
        } else if (/science/i.test(title)) {
            category = 'Science';
        } else if (/social/i.test(title)) {
            category = 'Social Science';
        } else if (/math/i.test(title)) {
            category = 'Math';
        }
        card.setAttribute('data-category', category);
    });

    // Category click event
    examCategoryList.forEach(li => {
        li.addEventListener('click', function() {
            // Remove active from all, add to clicked
            examCategoryList.forEach(cat => cat.classList.remove('active'));
            li.classList.add('active');

            // Get selected category
            const selected = li.querySelector('span').textContent.trim();

            // Show/hide cards
            examCards.forEach(card => {
                const cardCat = card.getAttribute('data-category');
                if (selected === 'All' || cardCat === selected) {
                    card.style.display = '';
                } else {
                    card.style.display = 'none';
                }
            });
        });
    });
});
    </script>

