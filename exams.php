    <script>
        document.addEventListener('DOMContentLoaded', function() {
            // Selectors for the exams section
            const examCategoryList = document.querySelectorAll('.exams-listing-section .category-list li');
            const examCards = document.querySelectorAll('.exams-listing-section .exam-card');

            // Assign data-category to each exam card based on its title
            examCards.forEach(card => {
                const title = card.querySelector('.exam-content h3').textContent;
                let category = 'Other';
                
                // AWS certifications
                if (/AWS|aws/i.test(title)) {
                    category = 'AWS';
                }
                // Cisco certifications
                else if (/Cisco|CCNA|CCNP/i.test(title)) {
                    category = 'Cisco';
                }
                // Microsoft certifications
                else if (/Microsoft|Azure|AI-/i.test(title)) {
                    category = 'Microsoft';
                }
                // CompTIA certifications
                else if (/CompTIA|A\+|Security\+/i.test(title)) {
                    category = 'CompTIA';
                }
                // ISACA certifications
                else if (/ISACA|CISA|CISM/i.test(title)) {
                    category = 'ISACA';
                }
                // Google certifications
                else if (/Google|Cloud Engineer/i.test(title)) {
                    category = 'Google';
                }
                // Salesforce certifications
                else if (/Salesforce/i.test(title)) {
                    category = 'Salesforce';
                }
                // ECCouncil certifications
                else if (/ECCouncil|CEH|312-/i.test(title)) {
                    category = 'ECCouncil';
                }
                // Palo Alto certifications
                else if (/Palo Alto|PCNSA/i.test(title)) {
                    category = 'Palo Alto';
                }
                
                card.setAttribute('data-category', category);
            });

            // Category click event
            examCategoryList.forEach(li => {
                li.addEventListener('click', function() {
                    // Remove active from all, add to clicked
                    examCategoryList.forEach(cat => cat.classList.remove('active'));
                    li.classList.add('active');

                    // Get selected category
                    const selected = li.querySelector('span').textContent.trim();

                    // Show/hide cards
                    examCards.forEach(card => {
                        const cardCat = card.getAttribute('data-category');
                        if (selected === 'All' || cardCat === selected) {
                            card.style.display = '';
                        } else {
                            card.style.display = 'none';
                        }
                    });
                });
            });
        });
    </script>

